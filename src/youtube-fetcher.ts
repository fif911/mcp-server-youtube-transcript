/**
 * Start background polling for a live stream
 */
export async function startLiveChatStream(videoId: string): Promise<LiveChatResult & { streaming: boolean }> {
  // Check if already streaming
  if (activeStreams.has(videoId)) {
    const state = activeStreams.get(videoId)!;
    // Return buffered messages and clear buffer
    const messages = [...state.messageBuffer];
    state.messageBuffer = [];

    return {
      messages,
      continuation: state.continuation,
      isLive: state.isPolling,
      pollIntervalMs: state.pollIntervalMs,
      videoTitle: state.videoTitle,
      channelName: state.channelName,
      streaming: true
    };
  }

  // Start new stream
  const result = await getLiveChat({ videoID: videoId });

  const state: LiveStreamState = {
    videoId,
    visitorData: result.visitorData,
    continuation: result.continuation || '',
    pollIntervalMs: result.pollIntervalMs,
    messageBuffer: [],
    seenIds: new Set(result.messages.map(m => m.id)),
    isPolling: true,
    pollTimer: null,
    videoTitle: result.videoTitle,
    channelName: result.channelName,
    startedAt: Date.now(),
    lastPollAt: Date.now(),
    totalMessagesFetched: result.messages.length
  };

  activeStreams.set(videoId, state);

  // Start background polling
  const poll = async () => {
    if (!state.isPolling) return;

    try {
      const newResult = await getLiveChat({
        videoID: videoId,
        continuation: state.continuation,
        visitorData: state.visitorData
      });

      state.lastPollAt = Date.now();
      state.continuation = newResult.continuation || state.continuation;
      state.pollIntervalMs = newResult.pollIntervalMs;

      // Count new messages before adding to seenIds
      let newMsgCount = 0;

      // Add only new messages to buffer
      for (const msg of newResult.messages) {
        if (!state.seenIds.has(msg.id)) {
          state.seenIds.add(msg.id);
          state.messageBuffer.push(msg);
          state.totalMessagesFetched++;
          newMsgCount++;
        }
      }

      // Cap buffer at 500 messages
      if (state.messageBuffer.length > 500) {
        state.messageBuffer = state.messageBuffer.slice(-500);
      }

      // Cap seenIds at 2000
      if (state.seenIds.size > 2000) {
        const arr = Array.from(state.seenIds);
        state.seenIds = new Set(arr.slice(-1500));
      }

      // Log new messages to stderr
      if (newMsgCount > 0) {
        process.stderr.write(`\n[LIVE] ${state.videoTitle || videoId} | +${newMsgCount} msgs | buffer: ${state.messageBuffer.length} | total: ${state.totalMessagesFetched}\n`);
        const lastMsg = state.messageBuffer[state.messageBuffer.length - 1];
        if (lastMsg) {
          process.stderr.write(`  └─ ${lastMsg.author}: ${lastMsg.text.slice(0, 60)}${lastMsg.text.length > 60 ? '...' : ''}\n`);
        }
      }

    } catch (err) {
      console.error(`[live-chat] Poll error for ${videoId}:`, err);
      // Stream may have ended
      if ((err as Error).message?.includes('ended')) {
        state.isPolling = false;
        return;
      }
    }

    // Schedule next poll
    if (state.isPolling) {
      state.pollTimer = setTimeout(poll, state.pollIntervalMs);
    }
  };

  // Start first background poll after initial delay
  state.pollTimer = setTimeout(poll, state.pollIntervalMs);

  return {
    ...result,
    streaming: true
  };
}

/**
 * Get buffered messages from an active stream
 */
export function getLiveChatBuffer(videoId: string): {
  messages: LiveChatMessage[];
  isStreaming: boolean;
  stats: {
    totalFetched: number;
    bufferSize: number;
    streamDurationMs: number;
    lastPollMs: number;
  };
  videoTitle?: string;
  channelName?: string;
} | null {
  const state = activeStreams.get(videoId);
  if (!state) return null;

  // Get and clear buffer
  const messages = [...state.messageBuffer];
  state.messageBuffer = [];

  return {
    messages,
    isStreaming: state.isPolling,
    stats: {
      totalFetched: state.totalMessagesFetched,
      bufferSize: messages.length,
      streamDurationMs: Date.now() - state.startedAt,
      lastPollMs: Date.now() - state.lastPollAt
    },
    videoTitle: state.videoTitle,
    channelName: state.channelName
  };
}

/**
 * Stop background polling for a live stream
 */
export function stopLiveChatStream(videoId: string): {
  stopped: boolean;
  finalMessages: LiveChatMessage[];
  stats: {
    totalFetched: number;
    streamDurationMs: number;
  };
} | null {
  const state = activeStreams.get(videoId);
  if (!state) return null;

  state.isPolling = false;
  if (state.pollTimer) {
    clearTimeout(state.pollTimer);
  }

  const finalMessages = [...state.messageBuffer];
  const stats = {
    totalFetched: state.totalMessagesFetched,
    streamDurationMs: Date.now() - state.startedAt
  };

  activeStreams.delete(videoId);

  return {
    stopped: true,
    finalMessages,
    stats
  };
}

