/**
 * Returns available caption languages for a video without fetching the full transcript
 */
export async function getAvailableLanguages(videoID: string): Promise<CaptionTrack[]> {
  if (!videoID || typeof videoID !== 'string') {
    throw new Error('Invalid video ID: must be a non-empty string');
  }
  const { availableLanguages } = await getPageData(videoID);
  return availableLanguages;
}

/**
 * Fetches just the comment count for a video without fetching all comments
 * Makes a single API call to get the count from the comments header
 */
export async function getCommentCount(videoID: string): Promise<number> {
  if (!videoID || typeof videoID !== 'string') {
    throw new Error('Invalid video ID: must be a non-empty string');
  }

  const { visitorData, clientVersion } = await getPageData(videoID);
  const continuationToken = encodeURIComponent(buildCommentsParams(videoID, 0));

  const payload = JSON.stringify({
    context: {
      client: {
        hl: 'en',
        gl: 'US',
        clientName: 'WEB',
        clientVersion: clientVersion,
        visitorData: visitorData
      }
    },
    continuation: continuationToken
  });

  try {
    const response = await httpsRequest({
      hostname: 'www.youtube.com',
      path: '/youtubei/v1/next?prettyPrint=false',
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Content-Length': Buffer.byteLength(payload),
        'User-Agent': WEB_USER_AGENT,
        'Origin': 'https://www.youtube.com'
      }
    }, payload);

    const json = JSON.parse(response);
    const endpoints = json.onResponseReceivedEndpoints || [];

    for (const endpoint of endpoints) {
      const items = endpoint.reloadContinuationItemsCommand?.continuationItems ||
                    endpoint.appendContinuationItemsAction?.continuationItems || [];
      for (const item of items) {
        if (item.commentsHeaderRenderer) {
          const countText = item.commentsHeaderRenderer.countText?.runs?.[0]?.text || '';
          const countMatch = countText.match(/[\d,]+/);
          if (countMatch) {
            return parseInt(countMatch[0].replace(/,/g, ''), 10);
          }
        }
      }
    }
  } catch (err) {
    console.error('[youtube-fetcher] Error fetching comment count:', err);
  }

  return 0;
}

/**
 * Fetches transcript using the YouTube internal API
 * If the requested language is not available and enableFallback is true,
 * it will try English first, then fall back to the first available language.
 */
export async function getSubtitles(options: {
  videoID: string;
  lang?: string;
  enableFallback?: boolean;
}): Promise<SubtitleResult> {
  const { videoID, lang = 'en', enableFallback = true } = options;

  // Validate video ID format
  if (!videoID || typeof videoID !== 'string') {
    throw new Error('Invalid video ID: must be a non-empty string');
  }

  // Get page data (visitor data needed for API authentication)
  console.error(`[youtube-fetcher] Fetching page data for video: ${videoID}`);
  const { visitorData, availableLanguages, chapters, adChapters, metadata, isLive } = await getPageData(videoID);
  console.error(`[youtube-fetcher] Page data: title="${metadata.title}", visitorData=${visitorData ? 'present' : 'MISSING'}, availableLanguages=[${availableLanguages.map(l => l.languageCode).join(',')}]`);

  // Determine which language to use and whether it's ASR
  let targetLang = lang;
  let isAsr = true; // Default to ASR for backwards compatibility

  if (availableLanguages.length > 0) {
    const requestedTrack = availableLanguages.find(t => t.languageCode === lang);
    const hasRequestedLang = !!requestedTrack;

    if (!hasRequestedLang && enableFallback) {
      // Try English first
      const englishTrack = availableLanguages.find(t => t.languageCode === 'en');
      if (englishTrack) {
        targetLang = 'en';
        isAsr = englishTrack.isAutoGenerated;
        console.error(`[youtube-fetcher] Language '${lang}' not available, falling back to 'en' (isAsr=${isAsr})`);
      } else {
        // Use first available
        targetLang = availableLanguages[0].languageCode;
        isAsr = availableLanguages[0].isAutoGenerated;
        console.error(`[youtube-fetcher] Language '${lang}' not available, falling back to '${targetLang}' (isAsr=${isAsr})`);
      }
    } else if (!hasRequestedLang) {
      throw new Error(`Language '${lang}' not available. Available: ${availableLanguages.map(t => t.languageCode).join(', ')}`);
    } else {
      isAsr = requestedTrack!.isAutoGenerated;
    }
  }

  console.error(`[youtube-fetcher] Building params for lang=${targetLang}, isAsr=${isAsr}`);

  // Build params and try multiple clients until we get segments
  const params = buildParams(videoID, targetLang, isAsr);

  const androidPayload = JSON.stringify({
    context: {
      client: {
        hl: targetLang,
        gl: 'US',
        clientName: 'ANDROID',
        clientVersion: ANDROID_CLIENT_VERSION,
        androidSdkVersion: 30,
        visitorData: visitorData
      }
    },
    params: params
  });

  // Try TVHTML5 (TV app) which often has different API behavior
  const tvPayload = JSON.stringify({
    context: {
      client: {
        hl: targetLang,
        gl: 'US',
        clientName: 'TVHTML5',
        clientVersion: '7.20211222.00.00',
        visitorData: visitorData
      }
    },
    params: params
  });

  // Try iOS client
  const iosPayload = JSON.stringify({
    context: {
      client: {
        hl: targetLang,
        gl: 'US',
        clientName: 'IOS',
        clientVersion: '19.29.1',
        visitorData: visitorData
      }
    },
    params: params
  });

  const payloads = [
    { name: 'ANDROID', payload: androidPayload, userAgent: ANDROID_USER_AGENT },
    { name: 'IOS', payload: iosPayload, userAgent: 'com.google.ios.youtube/19.29.1 (iPhone16,2; U; CPU iOS 17_5_1 like Mac OS X;)' },
    { name: 'TVHTML5', payload: tvPayload, userAgent: 'Mozilla/5.0 (SMART-TV; LINUX; Tizen 6.0) AppleWebKit/537.36 (KHTML, like Gecko) 80.0.3987.149/6.0 TV Safari/537.36' }
  ];

  // Try each client until we get segments
  let response: string = '';
  let json: any = null;
  let segments: any[] = [];
  let lastError: Error | null = null;

  for (const { name, payload, userAgent } of payloads) {
    try {
      console.error(`[youtube-fetcher] Trying ${name} client...`);
      response = await httpsRequest({
        hostname: 'www.youtube.com',
        path: '/youtubei/v1/get_transcript?prettyPrint=false',
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Content-Length': Buffer.byteLength(payload),
          'User-Agent': userAgent,
          'Origin': 'https://www.youtube.com'
        }
      }, payload);

      // Parse response
      try {
        json = JSON.parse(response);
      } catch (err) {
        console.error(`[youtube-fetcher] ${name} client: failed to parse response`);
        continue;
      }

      // Check for API-level errors
      if (json.error) {
        console.error(`[youtube-fetcher] ${name} client: API error: ${json.error.message || json.error.code}`);
        continue;
      }

      // Extract transcript segments - handle both WEB and ANDROID response formats
      const webSegments = json?.actions?.[0]?.updateEngagementPanelAction?.content
        ?.transcriptRenderer?.content?.transcriptSearchPanelRenderer?.body
        ?.transcriptSegmentListRenderer?.initialSegments;

      const androidSegments = json?.actions?.[0]?.elementsCommand?.transformEntityCommand
        ?.arguments?.transformTranscriptSegmentListArguments?.overwrite?.initialSegments;

      segments = webSegments || androidSegments || [];
      console.error(`[youtube-fetcher] ${name} client: found ${segments.length} segments`);

      if (segments.length > 0) {
        break; // Found segments, stop trying
      }
    } catch (err) {
      lastError = err as Error;
      console.error(`[youtube-fetcher] ${name} client error: ${(err as Error).message}`);
    }
  }

  if (!json) {
    throw new TranscriptFetchError(`Failed to fetch transcript API: ${lastError?.message || 'Unknown error'}`, isLive, metadata);
  }

  // If no segments found, check for continuation key in frameworkUpdates
  if (segments.length === 0) {
    console.error(`[youtube-fetcher] No segments in initial response, checking for continuation key...`);
    const fw = json?.frameworkUpdates;
    const mutations = fw?.entityBatchUpdate?.mutations || [];

    for (const mut of mutations) {
      const tsde = mut.payload?.transcriptSegmentsDataEntity;
      if (tsde?.key) {
        console.error(`[youtube-fetcher] Found continuation key: ${tsde.key}`);

        // Make a follow-up request with the continuation key
        const continuationPayload = JSON.stringify({
          context: {
            client: {
              hl: targetLang,
              gl: 'US',
              clientName: 'ANDROID',
              clientVersion: ANDROID_CLIENT_VERSION,
              androidSdkVersion: 30,
              visitorData: visitorData
            }
          },
          continuation: tsde.key
        });

        try {
          console.error(`[youtube-fetcher] Fetching segments with continuation key...`);
          const contResponse = await httpsRequest({
            hostname: 'www.youtube.com',
            path: '/youtubei/v1/get_transcript?prettyPrint=false',
            method: 'POST',
            headers: {
              'Content-Type': 'application/json',
              'Content-Length': Buffer.byteLength(continuationPayload),
              'User-Agent': ANDROID_USER_AGENT,
              'Origin': 'https://www.youtube.com'
            }
          }, continuationPayload);

          const contJson = JSON.parse(contResponse);
          console.error(`[youtube-fetcher] Continuation response keys: ${JSON.stringify(Object.keys(contJson))}`);

          // Try to extract segments from continuation response
          const contWebSegments = contJson?.actions?.[0]?.updateEngagementPanelAction?.content
            ?.transcriptRenderer?.content?.transcriptSearchPanelRenderer?.body
            ?.transcriptSegmentListRenderer?.initialSegments;

          const contAndroidSegments = contJson?.actions?.[0]?.elementsCommand?.transformEntityCommand
            ?.arguments?.transformTranscriptSegmentListArguments?.overwrite?.initialSegments;

          // Also check frameworkUpdates in continuation response
          const contMutations = contJson?.frameworkUpdates?.entityBatchUpdate?.mutations || [];
          let mutationSegments: any[] = [];
          for (const contMut of contMutations) {
            const segData = contMut.payload?.transcriptSegmentRenderer;
            if (segData) {
              mutationSegments.push({ transcriptSegmentRenderer: segData });
            }
          }

          segments = contWebSegments || contAndroidSegments || mutationSegments || [];
          console.error(`[youtube-fetcher] Continuation found ${segments.length} segments`);

          if (segments.length > 0) {
            break;
          }
        } catch (err) {
          console.error(`[youtube-fetcher] Continuation request failed: ${(err as Error).message}`);
        }
      }
    }
  }

  if (segments.length === 0) {
    throw new TranscriptFetchError('No transcript available for this video. The video may not have captions enabled.', isLive, metadata);
  }

  // Convert to TranscriptLine format
  const lines = segments
    .filter((seg: any) => seg?.transcriptSegmentRenderer) // Skip section headers
    .map((seg: any) => {
      const renderer = seg.transcriptSegmentRenderer;

      // Handle both WEB format (snippet.runs) and ANDROID format (snippet.elementsAttributedString)
      const webText = renderer?.snippet?.runs?.map((r: any) => r.text || '').join('');
      const androidText = renderer?.snippet?.elementsAttributedString?.content;
      const text = webText || androidText || '';

      const startMs = parseInt(renderer?.startMs || '0', 10);
      const endMs = parseInt(renderer?.endMs || '0', 10);

      return {
        text: text,
        start: startMs / 1000,
        dur: (endMs - startMs) / 1000
      };
    })
    .filter((line: TranscriptLine) => line.text.length > 0);

  // commentCount is already extracted from the page HTML in getPageData
  // No need for a separate API call that re-fetches the entire page

  return {
    lines,
    requestedLang: lang,
    actualLang: targetLang,
    availableLanguages,
    chapters,
    adChapters,
    metadata,
    isLive
  };
}

export interface CommentsResult {
  comments: Comment[];
  totalCount: number;
  continuation?: string;
}

interface CommentPageResult {
  comments: Comment[];
  totalCount: number;
  continuation?: string;
  replyTokens: Map<string, string>; // commentId -> reply continuation token
}

/**
 * Fetches a single page of comments using a continuation token
 */
async function fetchCommentsPage(
  continuationToken: string,
  visitorData: string,
  clientVersion: string
): Promise<CommentPageResult> {
  const payload = JSON.stringify({
    context: {
      client: {
        hl: 'en',
        gl: 'US',
        clientName: 'WEB',
        clientVersion: clientVersion,
        visitorData: visitorData
      }
    },
    continuation: continuationToken
  });

  const response = await httpsRequest({
    hostname: 'www.youtube.com',
    path: '/youtubei/v1/next?prettyPrint=false',
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Content-Length': Buffer.byteLength(payload),
      'User-Agent': WEB_USER_AGENT,
      'Origin': 'https://www.youtube.com'
    }
  }, payload);

  const json = JSON.parse(response);
  if (json.error) {
    throw new Error(`YouTube API error: ${json.error.message || 'Unknown error'}`);
  }

  const comments: Comment[] = [];
  let totalCount = 0;
  let nextContinuation: string | undefined;
  const replyTokens = new Map<string, string>();

  const endpoints = json.onResponseReceivedEndpoints || [];

  // Get total count, continuation, and reply tokens from endpoints
  for (const endpoint of endpoints) {
    const items = endpoint.reloadContinuationItemsCommand?.continuationItems ||
                  endpoint.appendContinuationItemsAction?.continuationItems || [];
    for (const item of items) {
      if (item.commentsHeaderRenderer) {
        const countText = item.commentsHeaderRenderer.countText?.runs?.[0]?.text || '';
        const countMatch = countText.match(/[\d,]+/);
        if (countMatch) {
          totalCount = parseInt(countMatch[0].replace(/,/g, ''), 10);
        }
      }
      if (item.continuationItemRenderer) {
        nextContinuation = item.continuationItemRenderer.continuationEndpoint?.continuationCommand?.token;
      }
      // Extract reply continuation tokens from comment threads
      if (item.commentThreadRenderer) {
        const thread = item.commentThreadRenderer;
        const vm = thread.commentViewModel?.commentViewModel;
        const commentId = vm?.commentId;
        const replies = thread.replies?.commentRepliesRenderer;
        if (commentId && replies) {
          // Look for continuation token in replies
          const replyContents = replies.contents || [];
          for (const rc of replyContents) {
            const token = rc.continuationItemRenderer?.continuationEndpoint?.continuationCommand?.token;
            if (token) {
              replyTokens.set(commentId, token);
              break;
            }
          }
        }
      }
    }
  }

  // Extract comments from frameworkUpdates (new YouTube API structure)
  const mutations = json.frameworkUpdates?.entityBatchUpdate?.mutations || [];
  const pinnedCommentIds = new Set<string>();

  // First pass: identify pinned comments from commentViewModel
  for (const endpoint of endpoints) {
    const items = endpoint.reloadContinuationItemsCommand?.continuationItems ||
                  endpoint.appendContinuationItemsAction?.continuationItems || [];
    for (const item of items) {
      const vm = item.commentThreadRenderer?.commentViewModel?.commentViewModel;
      if (vm?.pinnedText && vm.commentId) {
        pinnedCommentIds.add(vm.commentId);
      }
    }
  }

  // Second pass: extract comment data
  for (const mut of mutations) {
    const mutPayload = mut.payload?.commentEntityPayload;
    if (!mutPayload) continue;

    const props = mutPayload.properties || {};
    const author = mutPayload.author || {};
    const toolbar = mutPayload.toolbar || {};
    const commentId = props.commentId || '';

    comments.push({
      author: author.displayName || '',
      authorChannelId: author.channelId || '',
      text: props.content?.content || '',
      likes: toolbar.likeCountNotliked || '0',
      replyCount: parseInt(toolbar.replyCount || '0', 10),
      publishedTime: props.publishedTime || '',
      isPinned: pinnedCommentIds.has(commentId),
      isHearted: !!(toolbar.heartActiveTooltip),
      commentId,
      isReply: false
    });
  }

  return { comments, totalCount, continuation: nextContinuation, replyTokens };
}

/**
 * Fetches replies for a comment using its reply continuation token
 */
async function fetchReplies(
  replyToken: string,
  parentId: string,
  visitorData: string,
  clientVersion: string,
  maxReplies: number = 50
): Promise<Comment[]> {
  const replies: Comment[] = [];
  let token: string | undefined = replyToken;
  let pagesFetched = 0;
  const maxPages = 5;

  while (token && replies.length < maxReplies && pagesFetched < maxPages) {
    const payload = JSON.stringify({
      context: {
        client: {
          hl: 'en',
          gl: 'US',
          clientName: 'WEB',
          clientVersion: clientVersion,
          visitorData: visitorData
        }
      },
      continuation: token
    });

    try {
      const response = await httpsRequest({
        hostname: 'www.youtube.com',
        path: '/youtubei/v1/next?prettyPrint=false',
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Content-Length': Buffer.byteLength(payload),
          'User-Agent': WEB_USER_AGENT,
          'Origin': 'https://www.youtube.com'
        }
      }, payload);

      const json = JSON.parse(response);
      pagesFetched++;

      // Extract reply comments from mutations
      const mutations = json.frameworkUpdates?.entityBatchUpdate?.mutations || [];
      for (const mut of mutations) {
        const mutPayload = mut.payload?.commentEntityPayload;
        if (!mutPayload) continue;

        const props = mutPayload.properties || {};
        const author = mutPayload.author || {};
        const toolbar = mutPayload.toolbar || {};

        replies.push({
          author: author.displayName || '',
          authorChannelId: author.channelId || '',
          text: props.content?.content || '',
          likes: toolbar.likeCountNotliked || '0',
          replyCount: 0,
          publishedTime: props.publishedTime || '',
          isPinned: false,
          isHearted: !!(toolbar.heartActiveTooltip),
          commentId: props.commentId || '',
          isReply: true,
          parentId
        });
      }

      // Get next continuation token for more replies
      token = undefined;
      const endpoints = json.onResponseReceivedEndpoints || [];
      for (const endpoint of endpoints) {
        const items = endpoint.appendContinuationItemsAction?.continuationItems || [];
        for (const item of items) {
          if (item.continuationItemRenderer) {
            token = item.continuationItemRenderer.continuationEndpoint?.continuationCommand?.token;
            break;
          }
        }
        if (token) break;
      }
    } catch (err) {
      console.error('[youtube-fetcher] Error fetching replies:', err);
      break;
    }
  }

  return replies.slice(0, maxReplies);
}

/**
 * Fetches comments for a video using YouTube's internal API with pagination support
 */
export async function getComments(options: {
  videoID: string;
  sortBy?: 'TOP_COMMENTS' | 'NEWEST_FIRST';
  limit?: number;
}): Promise<CommentsResult> {
  const { videoID, sortBy = 'TOP_COMMENTS', limit = 20 } = options;

  if (!videoID || typeof videoID !== 'string') {
    throw new Error('Invalid video ID: must be a non-empty string');
  }

  // Get page data for visitorData
  const { visitorData, clientVersion } = await getPageData(videoID);

  // Build initial comments params (0 = TOP_COMMENTS, 1 = NEWEST_FIRST)
  const sortByNum = sortBy === 'NEWEST_FIRST' ? 1 : 0;
  let continuationToken: string | undefined = encodeURIComponent(buildCommentsParams(videoID, sortByNum));

  const allComments: Comment[] = [];
  const allReplyTokens = new Map<string, string>();
  let totalCount = 0;
  const noLimit = limit === 0; // 0 means fetch all

  try {
    // First pass: fetch all top-level comments and collect reply tokens
    while (continuationToken && (noLimit || allComments.length < limit)) {
      const pageResult = await fetchCommentsPage(continuationToken, visitorData, clientVersion);

      if (pageResult.totalCount > 0) {
        totalCount = pageResult.totalCount;
      }

      allComments.push(...pageResult.comments);
      continuationToken = pageResult.continuation;

      // Collect reply tokens
      for (const [commentId, token] of pageResult.replyTokens) {
        allReplyTokens.set(commentId, token);
      }

      // If we got no new comments, stop
      if (pageResult.comments.length === 0) break;
    }

    // Second pass: fetch replies for comments that have them (if we have room)
    if ((noLimit || allComments.length < limit) && allReplyTokens.size > 0) {
      const commentsWithReplies = allComments.filter(c => c.replyCount > 0 && allReplyTokens.has(c.commentId));

      // Collect all replies keyed by parent ID to avoid mutating allComments during iteration
      const repliesByParent = new Map<string, Comment[]>();
      let totalRepliesFetched = 0;

      for (const comment of commentsWithReplies) {
        if (!noLimit && (allComments.length + totalRepliesFetched) >= limit) break;

        const replyToken = allReplyTokens.get(comment.commentId);
        if (!replyToken) continue;

        const remainingSlots = noLimit ? comment.replyCount : Math.min(limit - allComments.length - totalRepliesFetched, comment.replyCount);
        if (remainingSlots <= 0) break;

        const replies = await fetchReplies(replyToken, comment.commentId, visitorData, clientVersion, remainingSlots);
        repliesByParent.set(comment.commentId, replies);
        totalRepliesFetched += replies.length;
      }

      // Merge replies after parent comments (iterate in reverse to keep indices stable)
      const parentIds = [...repliesByParent.keys()];
      for (let i = parentIds.length - 1; i >= 0; i--) {
        const parentId = parentIds[i];
        const replies = repliesByParent.get(parentId)!;
        const parentIndex = allComments.findIndex(c => c.commentId === parentId);
        if (parentIndex !== -1) {
          allComments.splice(parentIndex + 1, 0, ...replies);
        } else {
          allComments.push(...replies);
        }
      }
    }
  } catch (err) {
    console.error('[youtube-fetcher] Error fetching comments:', err);
  }

  return {
    comments: noLimit ? allComments : allComments.slice(0, limit),
    totalCount,
    continuation: continuationToken
  };
}

