const ANDROID_USER_AGENT = `com.google.android.youtube/${ANDROID_CLIENT_VERSION} (Linux; U; Android 11) gzip`;
const WEB_USER_AGENT = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36';
const DEFAULT_CLIENT_VERSION = '2.20251201.01.00'; // Fallback if not extracted from page

/**
 * Encodes a number as a protobuf varint
 * Handles lengths > 127 correctly (multi-byte encoding)
 */
function encodeVarint(value: number): number[] {
  const bytes: number[] = [];
  while (value > 0x7f) {
    bytes.push((value & 0x7f) | 0x80);
    value >>>= 7;
  }
  bytes.push(value);
  return bytes;
}

/**
 * Builds the protobuf-encoded params for the transcript API
 * @param isAsr - true for auto-generated captions (ASR), false for uploaded/manual captions
 */
function buildParams(videoId: string, lang: string = 'en', isAsr: boolean = true): string {
  // Inner protobuf: language params
  // For ASR captions: includes "asr" field
  // For uploaded captions: omits "asr" field
  const innerParts: number[] = isAsr
    ? [
        0x0a, 0x03, ...Buffer.from('asr'),           // Field 1, "asr" (only for auto-generated)
        0x12, ...encodeVarint(lang.length), ...Buffer.from(lang),  // Field 2, language code
        0x1a, 0x00                                    // Field 3, empty
      ]
    : [
        0x12, ...encodeVarint(lang.length), ...Buffer.from(lang),  // Field 2, language code
        0x1a, 0x00                                    // Field 3, empty
      ];
  const innerBuf = Buffer.from(innerParts);
  const innerB64 = innerBuf.toString('base64');
  const innerEncoded = encodeURIComponent(innerB64);

  // Outer protobuf
  const panelName = 'engagement-panel-searchable-transcript-search-panel';
  const outerParts: number[] = [
    0x0a, ...encodeVarint(videoId.length), ...Buffer.from(videoId),      // Field 1, video ID
    0x12, ...encodeVarint(innerEncoded.length), ...Buffer.from(innerEncoded), // Field 2, language params
    0x18, 0x01,                                          // Field 3, value 1
    0x2a, ...encodeVarint(panelName.length), ...Buffer.from(panelName),  // Field 5, panel name
    0x30, 0x01,                                          // Field 6, value 1
    0x38, 0x01,                                          // Field 7, value 1
    0x40, 0x01                                           // Field 8, value 1
  ];

  return Buffer.from(outerParts).toString('base64');
}

/**
 * Builds the protobuf-encoded params for the comments API
 * Structure: GetCommentsSectionParams from YouTube.js proto definitions
 */
function buildCommentsParams(videoId: string, sortBy: 0 | 1 = 0): string {
  // Helper to encode a string field
  const encodeString = (fieldNum: number, str: string): number[] => {
    const tag = (fieldNum << 3) | 2; // wire type 2 = length-delimited
    return [tag, ...encodeVarint(str.length), ...Buffer.from(str)];
  };

  // Helper to encode a varint field
  const encodeVarField = (fieldNum: number, value: number): number[] => {
    const tag = (fieldNum << 3) | 0; // wire type 0 = varint
    return [tag, ...encodeVarint(value)];
  };

  // Helper to encode a submessage
  const encodeSubmessage = (fieldNum: number, content: number[]): number[] => {
    const tag = (fieldNum << 3) | 2;
    return [tag, ...encodeVarint(content.length), ...content];
  };

  // Build Options (field 4 of Params)
  // video_id=4, sort_by=6, type=15
  const options: number[] = [
    ...encodeString(4, videoId),
    ...encodeVarField(6, sortBy),
    ...encodeVarField(15, 2)  // type = 2
  ];

  // Build Params (field 6)
  // opts=4, target=8
  const params: number[] = [
    ...encodeSubmessage(4, options),
    ...encodeString(8, 'comments-section')
  ];

  // Build Context (field 2)
  const ctx: number[] = [
    ...encodeString(2, videoId)
  ];

  // Build full GetCommentsSectionParams
  // ctx=2, unk_param=3, params=6
  const full: number[] = [
    ...encodeSubmessage(2, ctx),
    ...encodeVarField(3, 6),  // unk_param = 6
    ...encodeSubmessage(6, params)
  ];

  return Buffer.from(full).toString('base64');
}

/**
 * Makes an HTTPS request and returns the response body
 * Includes timeout and HTTP status code validation
 */
function httpsRequest(options: https.RequestOptions, data?: string): Promise<string> {
  return new Promise((resolve, reject) => {
    const req = https.request({ ...options, timeout: REQUEST_TIMEOUT }, (res) => {
      // Validate HTTP status code
      if (res.statusCode && (res.statusCode < 200 || res.statusCode >= 300)) {
        reject(new Error(`HTTP ${res.statusCode}: ${res.statusMessage || 'Unknown error'}`));
        return;
      }

      let body = '';
      res.on('data', chunk => body += chunk);
      res.on('end', () => resolve(body));
    });

    req.on('error', (err) => {
      reject(new Error(`Network error: ${err.message}`));
    });

    req.on('timeout', () => {
      req.destroy();
      reject(new Error(`Request timeout after ${REQUEST_TIMEOUT}ms`));
    });

    if (data) req.write(data);
    req.end();
  });
}

/**
 * Fetches the YouTube video page and extracts visitor data and client version
 */
async function getPageData(videoId: string): Promise<PageData> {
  let html: string;

  try {
    html = await httpsRequest({
      hostname: 'www.youtube.com',
      path: `/watch?v=${videoId}`,
      method: 'GET',
      headers: {
        'User-Agent': WEB_USER_AGENT,
        'Accept-Language': 'en-US,en;q=0.9'
      }
    });
  } catch (err) {
    throw new Error(`Failed to fetch video page: ${(err as Error).message}`);
  }

  // Check for video availability issues
  if (html.includes('"status":"LOGIN_REQUIRED"') ||
      html.includes('"reason":"Sign in to confirm your age') ||
      html.includes('ageGate') ||
      html.includes('"AGE_VERIFICATION_REQUIRED"')) {
    throw new Error('This video is age-restricted and requires sign-in to view. Transcripts cannot be retrieved for age-restricted content.');
  }

  if (html.includes('"status":"UNPLAYABLE"') || html.includes('"status":"ERROR"')) {
    // Extract reason if available
    const reasonMatch = html.match(/"reason":"([^"]+)"/);
    const reason = reasonMatch?.[1] || 'Video is unavailable';
    throw new Error(`Video unavailable: ${reason}`);
  }

  if (html.includes('"isPrivate":true')) {
    throw new Error('This video is private and cannot be accessed.');
  }

  if (html.includes('"status":"LIVE_STREAM_OFFLINE"')) {
    throw new Error('This is a live stream that is currently offline. No transcript available.');
  }

  // Extract visitor data
  const visitorMatch = html.match(/"visitorData":"([^"]+)"/);
  const visitorData = visitorMatch?.[1] || '';

  if (!visitorData) {
    console.error(`[youtube-fetcher] Warning: Could not extract visitorData for video ${videoId}. Request may fail.`);
  }

  // Extract client version (format: "2.YYYYMMDD.XX.XX")
  const versionMatch = html.match(/"clientVersion":"([\d.]+)"/);
  const clientVersion = versionMatch?.[1] || DEFAULT_CLIENT_VERSION;

  // Extract available caption tracks
  const availableLanguages: CaptionTrack[] = [];
  const captionsMatch = html.match(/"captions":\{"playerCaptionsTracklistRenderer":\{"captionTracks":(\[[^\]]+\])/);
  if (captionsMatch) {
    try {
      const tracks = JSON.parse(captionsMatch[1]);
      for (const track of tracks) {
        if (track.languageCode) {
          availableLanguages.push({
            languageCode: track.languageCode,
            name: track.name?.simpleText || track.name?.runs?.[0]?.text || track.languageCode,
            isAutoGenerated: track.kind === 'asr'
          });
        }
      }
    } catch (e) {
      console.error(`[youtube-fetcher] Warning: Could not parse caption tracks for video ${videoId}`);
    }
  }

  // Extract chapters and identify ad chapters
  const adChapters: AdChapter[] = [];
  const adMarkers = [
    '(werbung)', '(ad)', '(ads)', '(sponsor)', '(sponsored)',
    '(promo)', '(promotion)', '(anzeige)', '(reklame)',
    '[werbung]', '[ad]', '[ads]', '[sponsor]', '[sponsored]',
    '[promo]', '[promotion]', '[anzeige]', '[reklame]'
  ];

  // Extract chapters from chapterRenderer elements using individual field matches
  // Each chapterRenderer has a title and timeRangeStartMillis - extract all instances
  const chapters: Chapter[] = [];
  let searchPos = 0;
  while (true) {
    const chapterIdx = html.indexOf('"chapterRenderer"', searchPos);
    if (chapterIdx === -1) break;

    // Find the next chapterRenderer boundary (or end of string) for scoping
    const nextChapterIdx = html.indexOf('"chapterRenderer"', chapterIdx + 1);
    const chapterBlock = html.slice(chapterIdx, nextChapterIdx !== -1 ? nextChapterIdx : chapterIdx + 2000);

    // Extract title - try simpleText first, then runs[0].text
    const titleSimple = chapterBlock.match(/"title":\s*\{\s*"simpleText":\s*"([^"]+)"/);
    const titleRuns = chapterBlock.match(/"title":\s*\{\s*"runs":\s*\[\s*\{\s*"text":\s*"([^"]+)"/);
    const title = titleSimple?.[1] || titleRuns?.[1] || '';

    // Extract time
    const timeMatch = chapterBlock.match(/"timeRangeStartMillis":\s*"?(\d+)"?/);
    const startMs = timeMatch ? parseInt(timeMatch[1], 10) : -1;

    if (title && startMs >= 0) {
      chapters.push({
        title,
        startMs,
        isAd: adMarkers.some(marker => title.toLowerCase().includes(marker))
      });
    }

    searchPos = chapterIdx + 1;
  }

  // Convert ad chapters to AdChapter format with end times
  for (let i = 0; i < chapters.length; i++) {
    if (chapters[i].isAd) {
      const nextChapter = chapters[i + 1];
      // End time is start of next chapter, or +5 minutes if last chapter
      const endMs = nextChapter ? nextChapter.startMs : chapters[i].startMs + 300000;
      adChapters.push({
        title: chapters[i].title,
        startMs: chapters[i].startMs,
        endMs: endMs
      });
    }
  }

  // Extract video metadata (target videoDetails block, use .*? for nested objects)
  const titleMatch = html.match(/"videoDetails":\{.*?"title":"([^"]+)"/);
  const authorMatch = html.match(/"videoDetails":\{.*?"author":"([^"]+)"/);
  const subsMatch = html.match(/"subscriberCountText":\{"accessibility":\{"accessibilityData":\{"label":"([^"]+)"/);
  const viewsMatch = html.match(/"viewCount":"(\d+)"/);
  const dateMatch = html.match(/"publishDate":"([^"]+)"/);
  const lengthMatch = html.match(/"videoDetails":\{.*?"lengthSeconds":"(\d+)"/);
  
  // Extract comment count (may be in different locations in the HTML)
  const commentCountMatch = html.match(/"commentCount":"?(\d+)"?/) || 
                            html.match(/"commentCountText":\{"runs":\[\{"text":"([\d,]+)"/);

  // Shorten subscriber count: "649 thousand subscribers" → "649k"
  let subs = subsMatch?.[1] || '';
  subs = subs.replace(/ subscribers?/i, '').replace(/ thousand/i, 'k').replace(/ million/i, 'M').replace(/ billion/i, 'B');

  // Format view count and comment count
  const viewNum = parseInt(viewsMatch?.[1] || '', 10);
  const viewsFormatted = formatCount(viewNum);

  const commentNum = parseInt((commentCountMatch?.[1] || '').replace(/,/g, ''), 10);
  const commentsFormatted = formatCount(commentNum);

  // Shorten date: "2025-12-03T03:01:16-08:00" → "2025-12-03"
  const dateRaw = dateMatch?.[1] || '';
  const dateShort = dateRaw.split('T')[0];

  // Format duration: 212 → "3:32", 3661 → "1:01:01"
  const lengthSecs = parseInt(lengthMatch?.[1] || '0', 10);
  let durationFormatted = '';
  if (lengthSecs > 0) {
    const hrs = Math.floor(lengthSecs / 3600);
    const mins = Math.floor((lengthSecs % 3600) / 60);
    const secs = lengthSecs % 60;
    durationFormatted = hrs > 0
      ? `${hrs}:${mins.toString().padStart(2, '0')}:${secs.toString().padStart(2, '0')}`
      : `${mins}:${secs.toString().padStart(2, '0')}`;
  }

  const metadata: VideoMetadata = {
    title: titleMatch?.[1] || '',
    author: authorMatch?.[1] || '',
    subscriberCount: subs,
    viewCount: viewsFormatted,
    commentCount: commentsFormatted,
    publishDate: dateShort,
    duration: durationFormatted
  };

  // Detect live streams
  const isLive = html.includes('"isLiveContent":true') || html.includes('"isLive":true');

  return { visitorData, clientVersion, availableLanguages, chapters, adChapters, metadata, isLive };
}

