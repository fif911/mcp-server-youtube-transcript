interface LiveStreamState {
  videoId: string;
  visitorData: string;
  continuation: string;
  pollIntervalMs: number;
  messageBuffer: LiveChatMessage[];
  seenIds: Set<string>;
  isPolling: boolean;
  pollTimer: NodeJS.Timeout | null;
  videoTitle?: string;
  channelName?: string;
  startedAt: number;
  lastPollAt: number;
  totalMessagesFetched: number;
}

// Global state for background streams
const activeStreams: Map<string, LiveStreamState> = new Map();

